// Import the data to customize and insert them into page
const fetchData = () => {
  fetch("customize.json")
    .then(data => data.json())
    .then(data => {
      dataArr = Object.keys(data);
      dataArr.map(customData => {
        if (data[customData] !== "") {
          if (customData === "imagePath") {
            document
              .querySelector(`[data-node-name*="${customData}"]`)
              .setAttribute("src", data[customData]);
          } else {
            document.querySelector(`[data-node-name*="${customData}"]`).innerText = data[customData];
          }
        }

        // 检查是否已遍历到最后一个元素，准备开始动画
        if (dataArr.length === dataArr.indexOf(customData) + 1) {
          animationTimeline(); // 开始动画时间线
        }
      });
    });
};

// 新增函数：读取照片并依次展示
const fetchPhotos = (onComplete) => {
  fetch("photos.json") // 从 photos.json 文件中获取照片列表
    .then(response => response.json())
    .then(data => {
      const photoGallery = document.querySelector(".photo-gallery"); // 获取照片展示容器
      const photoUrls = data.photos; // 照片列表

      // 清空照片容器，避免重复添加
      photoGallery.innerHTML = "";

      const tlPhotos = new TimelineMax({
        onComplete: onComplete // 当所有照片展示完成后，继续后续动画
      });

      photoUrls.forEach((photoUrl, index) => {
        const img = document.createElement("img");
        img.src = photoUrl;
        img.classList.add("photo");

        // 初始状态设置为不可见
        img.style.opacity = 0;

        // 将图片元素添加到照片容器中
        photoGallery.appendChild(img);

        // 将动画添加到照片展示时间线
        tlPhotos.to(img, 0.5, {
          opacity: 1,       // 渐显
          ease: Power2.easeInOut
        }, "+=1"); // 每张照片之间间隔 1 秒
      });
    })
    .catch(error => {
      console.error("Error loading photos:", error);
    });
};


